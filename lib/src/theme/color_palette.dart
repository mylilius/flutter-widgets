library mylilius_widgets;

abstract class ColorPalette {
  final String primaryColor;
  final String secondaryColor;
  final String accent;
  final String primaryBackground;
  final String secondaryBackground;
  final String green;
  final String red;
  final String brightness;

  ColorPalette(this.primaryColor, this.secondaryColor, this.primaryBackground, this.secondaryBackground, this.accent, this.green, this.red, this.brightness);
}

